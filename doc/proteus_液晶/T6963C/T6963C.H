/*
	LCD端口定义
	
		ATMega128					T6963C
		
			PA0			----		  D0
			PA1			----		  D1
			PA2			----		  D2
			PA3			----		  D3
			PA4			----		  D4
			PA5			----		  D5
			PA6			----		  D6
			PA7			----		  D7
			PC0			----		  C\D
			PC1			----		  \CE
			PC2			----		  RST
			RD			----		  RD
			WR			----		  WR
*/
#define CADDR (*(volatile unsigned char *)0x1500) 		//指令总线地址
#define DADDR (*(volatile unsigned char *)0x1400)		//数据总线地址
/**********************************
	延时函数:		Del             
	内部调用函数:	无  
	函数调用入口:	Del()        
	函数参数:		无               
	函数调用变量：	无       
***********************************/
void Del(unsigned int j)
{
	while(j--);
}
/**********************************
	读写忙判断函数:	st01             
	内部调用函数:	无  
	函数调用入口:	st01()        
	函数参数:		无               
	函数调用变量：	无       
***********************************/
void st01(void)
{
	unsigned char busy;
	do
	{
		busy=CADDR;
	}
	while((busy&0x03)!=0x03);
}
/**********************************
	写数据函数:		Write_Data            
	内部调用函数:	st01()  
	函数调用入口:	Write_Data(dat)        
	函数参数:		dat              
	函数调用变量：	无       
***********************************/
void Write_Data(unsigned char dat)
{
	st01();
	DADDR=dat;
}
/**********************************
	写无参数指令函数:	Write_Command             
	内部调用函数:		st01(); 
	函数调用入口:		Write_Command(cmd)      
	函数参数:			cmd              
	函数调用变量：		无       
***********************************/
void Write_Command(unsigned char cmd)
{
	st01();
	CADDR=cmd;
}
/**********************************
	写单参数指令函数:	Write_Command1             
	内部调用函数:		Write_Data(),Write_Command() 
	函数调用入口:		Write_Command(dat,cmd)      
	函数参数:			dat,cmd             
	函数调用变量：		无       
***********************************/
void Write_Command1(unsigned char dat,unsigned char cmd)
{
	Write_Data(dat);
	Write_Command(cmd);
}
/**********************************
	写双参数指令函数:	Write_Command2             
	内部调用函数:		Write_Data(),Write_Command()
	函数调用入口:		Write_Command2(dat,dat1,cmd)      
	函数参数:			dat,dat1,cmd              
	函数调用变量：		无        
***********************************/
void Write_Command2(unsigned char dat,unsigned char dat1,unsigned char cmd)
{
	Write_Data(dat);
	Write_Data(dat1);
	Write_Command(cmd);
}
/**********************************
	端口初始化函数:		Lcd_Port_Init             
	内部调用函数:		无  
	函数调用入口:		Lcd_Port_Init()     
	函数参数:			无               
	函数调用变量：		无       
***********************************/
void Lcd_Port_Init(void)
{
	MCUCR = 0x80; // 允许外部并行扩展接口 
	XMCRA = 0x00; // external memory 
	XMCRB = 0x05; // 释放PC7,PC6，PC5，PC4，PC3，作为通用I/O引脚使用 
}
/**********************************
	LCD初始化函数:		Lcd_Init             
	内部调用函数:		无  
	函数调用入口:		Lcd_Init()     
	函数参数:			无               
	函数调用变量：		无           
***********************************/
void Lcd_Init(void)
{
	DDRC=0xff;
	PORTC=0x00;			//LCD 复位
	Del(50000);
	PORTC=0xff;			//LCD 复位结束
	Lcd_Port_Init();
	Del(50000);
	Write_Command2( 0x00,0x00,0x42 ); //设置图形显示区域首地址GraphAddr
	Write_Command2( 30,0x00,0x43 ); //设置图形显示区域宽度 : LineChar
// 	Wrint_Command2( 0x00,0x00,0x42 ); //设置图形显示区域首地址GraphAddr
// 	Wrint_Command2( 0x10,0x00,0x43); //设置图形显示区域宽度:10H
	Write_Command(0xa7); //设置光标形状 cursor size 8x8
	Write_Command(0x80); //设置显示方式: CGROM方式 文本\图形 "或"
	Write_Command(0x98); //设置显示开关: 1001 1100 图形
}
